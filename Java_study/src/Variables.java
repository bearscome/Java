public class Variables {
    public static void main(String[] args){
        int a; // 정수
        a = 1;

        System.out.println(a);

        a = 2;
        System.out.println(a);

        double b; // 실수
        b = 1.1;

        System.out.println(b);


        String c;
        String d = "efgh";
        c = "abcd";

        System.out.println(c + d);
    }
}
